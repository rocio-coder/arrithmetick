public class Arithmetic {
    public void sum(int a, int b, Result resultado){
        resultado.setResultSum(a + b);
    }
    public void subtraction(int a, int b, Result resultado){
        resultado.setResultSubtraction(a - b);
    }
    public void multiplication(int a, int b, Result resultado){
        resultado.setMultiplication(a * b);
    }
    public void division(int a, int b, Result resultado){
        resultado.setDivision(a / b);
    }
}


